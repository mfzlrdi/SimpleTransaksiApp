    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        try {
            if (idbeli.getText().equals("") || pilihanbarang.getSelectedItem().equals("")
                    || hargabarang.getText().equals("") || qty.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Lengkapi Data", "Stroe Saya", JOptionPane.INFORMATION_MESSAGE);
                String a = qty.getText();
                int aa = Integer.parseInt(a);
                String b = tStok.getText();
                int bb = Integer.parseInt(b);
                if (aa > bb) {
                    JOptionPane.showMessageDialog(null, "Qty Melebihi Stok", "Stroe Saya",
                            JOptionPane.INFORMATION_MESSAGE);
                    qty.setText("");
                }
            } else {
                if (qty.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Isi Qty");
                } else {
                    int tqty, harga, jumlah;

                    tqty = Integer.parseInt(qty.getText().toString());
                    String hargaBersih = hargabarang.getText().replace(".", "");
                    harga = (int) Double.parseDouble(hargaBersih);
                    jumlah = tqty * harga;

                    DecimalFormatSymbols indo = new DecimalFormatSymbols();
                    indo.setGroupingSeparator('.');
                    DecimalFormat df = new DecimalFormat("#,###", indo);
                    totalbeli.setText(df.format(jumlah));

                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }// GEN-LAST:event_btnHitungActionPerformed

