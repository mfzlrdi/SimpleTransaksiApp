    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSelesaiActionPerformed
        // TODO add your handling code here:
        if (idbeli.getText().equals("") || idBarang.getText().equals("") || pilihanbarang.getSelectedItem().equals("")
                || totalbeli.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Lengkapi Data", "Stroe Saya", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String idPem = idbeli.getText();
            String idbarang = idBarang.getText();
            String namapem = namabeli.getText();
            String nama = (String) pilihanbarang.getSelectedItem();
            String harga = hargabarang.getText().replace(".", "");
            String tqty = qty.getText();
            String jumlah = totalbeli.getText().replace(".", "");
            try {
                String sql = "INSERT INTO transaksi (idPem, idBarang, namapem, namaBarang, hargaBarang, qty, totalBelanja) VALUES (?,?,?,?,?,?,?)";

                try (java.sql.PreparedStatement p = con.prepareStatement(sql)) {
                    p.setString(1, idPem);
                    p.setString(2, idbarang);
                    p.setString(3, namapem);
                    p.setString(4, nama);
                    p.setString(5, harga);
                    p.setString(6, tqty);
                    p.setString(7, jumlah);
                    int baris = p.executeUpdate();

                    if (baris > 0) {
                        idBarang.setText("");
                        namabeli.setText("");
                        pilihanbarang.setSelectedItem("");
                        hargabarang.setText("");
                        qty.setText("");
                        totalbeli.setText("");
                        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan", "Elektronik",
                                JOptionPane.INFORMATION_MESSAGE);
                        noFaktur();
                        tampilData("");
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan", "Elektronik",
                                JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Error: " + e.getMessage(), "Elektronik",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }// GEN-LAST:event_btnSelesaiActionPerformed

