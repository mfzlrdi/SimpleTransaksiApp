    private void tampilData(String where) {
        try {
            stat = con.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.getDataVector();
            tabModel.fireTableDataChanged();
            rs = stat.executeQuery("SELECT * FROM transaksi" + where);

            DecimalFormatSymbols indo = new DecimalFormatSymbols();
            indo.setGroupingSeparator('.');
            DecimalFormat df = new DecimalFormat("#,###", indo);

            while (rs.next()) {
                Object[] data = {
                        rs.getString("idPem"),
                        rs.getString("idBarang"),
                        rs.getString("namapem"),
                        rs.getString("namaBarang"),
                        df.format(rs.getDouble("hargaBarang")),
                        rs.getString("qty"),
                        df.format(rs.getDouble("totalBelanja"))
                };
                tabModel.addRow(data);
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

    private void noFaktur() {
        try {
            stat = con.createStatement();
            rs = stat.executeQuery("SELECT * FROM transaksi ORDER by idPem desc");
            if (rs.next()) {
                String nofak = rs.getString("idPem").substring(1);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String nol = "";

                switch (AN.length()) {
                    case 1:
                        nol = "000";
                        break;
                    case 2:
                        nol = "00";
                        break;
                    case 3:
                        nol = "0";
                        break;
                    case 4:
                        nol = "";
                        break;
                }

                idbeli.setText("F" + nol + AN);
            } else {
                idbeli.setText("F0001");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

