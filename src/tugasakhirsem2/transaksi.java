    private void pilihanbarangActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_pilihanbarangActionPerformed
        // TODO add your handling code here:
        if (pilihanbarang.getSelectedItem().equals("Pilih Barang")) {
            idBarang.setText("");
            hargabarang.setText("");
        } else {
            try {
                stat = con.createStatement();
                rs = stat.executeQuery("SELECT idBarang, jumlahBarang FROM barang WHERE namaBarang ='"
                        + pilihanbarang.getSelectedItem() + "'");
                while (rs.next()) {
                    idBarang.setText(rs.getString("idBarang"));
                    tStok.setText(rs.getString("jumlahBarang"));

                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
            try {
                stat = con.createStatement();
                rs = stat.executeQuery(
                        "SELECT hargaBarang FROM barang WHERE namaBarang ='" + pilihanbarang.getSelectedItem() + "'");
                DecimalFormatSymbols indo = new DecimalFormatSymbols();
                indo.setGroupingSeparator('.');
                DecimalFormat df = new DecimalFormat("#,###", indo);
                while (rs.next()) {
                    hargabarang.setText(df.format(rs.getDouble("hargaBarang")));
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }// GEN-LAST:event_pilihanbarangActionPerformed

